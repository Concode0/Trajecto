"""
Create HPO Dataset - Mixed Sim-Real Dataset for Hyperparameter Optimization

This script creates a small mixed dataset by sampling from:
1. Simulated data (generated by SimGenerator/)
2. Real acquired data (from iPad + ESP32)

The mixed dataset is used for fast HPO iterations before full-scale training.
"""

import argparse
import random
from pathlib import Path
from typing import List, Tuple

import h5py
import numpy as np
from tqdm import tqdm

from model.config import Config


def list_samples(h5_file: h5py.File) -> List[str]:
    """List all sample keys in an HDF5 file.

    Args:
        h5_file: Open HDF5 file handle

    Returns:
        List of sample keys (e.g., ['sample_001', 'sample_002', ...])
    """
    return [key for key in h5_file.keys() if key.startswith('sample_')]


def validate_sample(h5_file: h5py.File, sample_key: str) -> bool:
    """Validate that a sample has required datasets.

    Args:
        h5_file: Open HDF5 file handle
        sample_key: Sample key to validate

    Returns:
        True if valid, False otherwise
    """
    required_datasets = ['sensor_data', 'gt_pos_data', 'gt_vel_data']

    try:
        sample_group = h5_file[sample_key]
        for dataset in required_datasets:
            if dataset not in sample_group:
                return False

        # Check shapes are consistent
        sensor = sample_group['sensor_data']
        gt_pos = sample_group['gt_pos_data']
        gt_vel = sample_group['gt_vel_data']

        if sensor.shape[0] != gt_pos.shape[0] or sensor.shape[0] != gt_vel.shape[0]:
            return False

        # Check expected channels
        if sensor.shape[1] != Config.VALIDATION.EXPECTED_SENSOR_CHANNELS:
            return False

        if gt_pos.shape[1] != Config.VALIDATION.EXPECTED_SPATIAL_DIMS:
            return False

        if gt_vel.shape[1] != Config.VALIDATION.EXPECTED_SPATIAL_DIMS:
            return False

        return True

    except (KeyError, IndexError):
        return False


def copy_sample(
    src_file: h5py.File,
    dst_file: h5py.File,
    src_key: str,
    dst_key: str,
    source_label: str
) -> None:
    """Copy a sample from source to destination HDF5 file.

    Args:
        src_file: Source HDF5 file
        dst_file: Destination HDF5 file
        src_key: Source sample key
        dst_key: Destination sample key
        source_label: Label indicating source ('sim' or 'real')
    """
    src_group = src_file[src_key]
    dst_group = dst_file.create_group(dst_key)

    # Copy datasets
    dst_group.create_dataset('sensor_data', data=src_group['sensor_data'][:])
    dst_group.create_dataset('gt_pos_data', data=src_group['gt_pos_data'][:])
    dst_group.create_dataset('gt_vel_data', data=src_group['gt_vel_data'][:])

    # Copy gravity_b if available (from Apple Pencil pose)
    if 'gt_gravity_b_data' in src_group:
        dst_group.create_dataset('gt_gravity_b_data', data=src_group['gt_gravity_b_data'][:])

    # Copy attributes
    if 'sequence_length' in src_group.attrs:
        dst_group.attrs['sequence_length'] = src_group.attrs['sequence_length']
    else:
        # Compute sequence length if not present
        dst_group.attrs['sequence_length'] = src_group['sensor_data'].shape[0]

    # Add source label for tracking
    dst_group.attrs['source'] = source_label

    # Copy original label if exists
    if 'original_label' in src_group.attrs:
        dst_group.attrs['original_label'] = src_group.attrs['original_label']


def create_hpo_dataset(
    sim_dataset_path: str,
    real_dataset_path: str,
    output_path: str,
    num_samples: int = 100,
    sim_ratio: float = 0.8,
    seed: int = 42
) -> None:
    """Create mixed HPO dataset by sampling from sim and real datasets.

    Args:
        sim_dataset_path: Path to simulated dataset HDF5
        real_dataset_path: Path to real dataset HDF5
        output_path: Path to output HPO dataset HDF5
        num_samples: Total number of samples in HPO dataset
        sim_ratio: Ratio of simulated samples (0.0 to 1.0)
        seed: Random seed for reproducibility
    """
    print(f"\n{'='*70}")
    print(f"Creating HPO Dataset")
    print(f"{'='*70}")
    print(f"Configuration:")
    print(f"  Total samples: {num_samples}")
    print(f"  Sim ratio: {sim_ratio:.1%} ({int(num_samples * sim_ratio)} samples)")
    print(f"  Real ratio: {1-sim_ratio:.1%} ({int(num_samples * (1-sim_ratio))} samples)")
    print(f"  Random seed: {seed}")
    print(f"{'='*70}\n")

    # Set random seed
    random.seed(seed)
    np.random.seed(seed)

    # Check input files exist
    if not Path(sim_dataset_path).exists():
        raise FileNotFoundError(
            f"Simulated dataset not found at {sim_dataset_path}\n"
            f"Generate simulated data first: python utils/generate_sim_data.py"
        )

    if not Path(real_dataset_path).exists():
        raise FileNotFoundError(
            f"Real dataset not found at {real_dataset_path}\n"
            f"Run data acquisition first: python utils/acquire.py"
        )

    # Calculate sample counts
    num_sim = int(num_samples * sim_ratio)
    num_real = num_samples - num_sim

    print(f"Loading datasets...")

    # Open input files
    with h5py.File(sim_dataset_path, 'r') as sim_file, \
         h5py.File(real_dataset_path, 'r') as real_file:

        # List all samples
        sim_samples = list_samples(sim_file)
        real_samples = list_samples(real_file)

        print(f"  Simulated dataset: {len(sim_samples)} samples available")
        print(f"  Real dataset: {len(real_samples)} samples available")

        # Validate we have enough samples
        if len(sim_samples) < num_sim:
            print(f"  ⚠️  Warning: Not enough sim samples ({len(sim_samples)} < {num_sim})")
            print(f"     Using all {len(sim_samples)} available sim samples")
            num_sim = len(sim_samples)
            num_real = num_samples - num_sim

        if len(real_samples) < num_real:
            print(f"  ⚠️  Warning: Not enough real samples ({len(real_samples)} < {num_real})")
            print(f"     Using all {len(real_samples)} available real samples")
            num_real = len(real_samples)

        # Update total
        actual_total = num_sim + num_real
        actual_sim_ratio = num_sim / actual_total if actual_total > 0 else 0

        print(f"\nActual mix:")
        print(f"  Sim: {num_sim} ({actual_sim_ratio:.1%})")
        print(f"  Real: {num_real} ({1-actual_sim_ratio:.1%})")
        print(f"  Total: {actual_total}")

        # Randomly sample from each dataset
        print(f"\nSampling datasets...")
        selected_sim = random.sample(sim_samples, num_sim) if num_sim > 0 else []
        selected_real = random.sample(real_samples, num_real) if num_real > 0 else []

        # Validate selected samples
        print(f"Validating samples...")
        valid_sim = [s for s in tqdm(selected_sim, desc="Validating sim")
                     if validate_sample(sim_file, s)]
        valid_real = [s for s in tqdm(selected_real, desc="Validating real")
                      if validate_sample(real_file, s)]

        if len(valid_sim) < len(selected_sim):
            print(f"  ⚠️  Removed {len(selected_sim) - len(valid_sim)} invalid sim samples")

        if len(valid_real) < len(selected_real):
            print(f"  ⚠️  Removed {len(selected_real) - len(valid_real)} invalid real samples")

        # Create output directory if needed
        output_dir = Path(output_path).parent
        output_dir.mkdir(parents=True, exist_ok=True)

        # Create output file and copy samples
        print(f"\nCreating HPO dataset: {output_path}")

        with h5py.File(output_path, 'w') as out_file:
            sample_idx = 0

            # Copy simulated samples
            print(f"Copying {len(valid_sim)} simulated samples...")
            for src_key in tqdm(valid_sim, desc="Copying sim"):
                dst_key = f"sample_{sample_idx:03d}"
                copy_sample(sim_file, out_file, src_key, dst_key, source_label='sim')
                sample_idx += 1

            # Copy real samples
            print(f"Copying {len(valid_real)} real samples...")
            for src_key in tqdm(valid_real, desc="Copying real"):
                dst_key = f"sample_{sample_idx:03d}"
                copy_sample(real_file, out_file, src_key, dst_key, source_label='real')
                sample_idx += 1

            # Add metadata
            out_file.attrs['num_samples'] = sample_idx
            out_file.attrs['num_sim_samples'] = len(valid_sim)
            out_file.attrs['num_real_samples'] = len(valid_real)
            out_file.attrs['sim_ratio'] = len(valid_sim) / sample_idx if sample_idx > 0 else 0
            out_file.attrs['seed'] = seed
            out_file.attrs['sim_source'] = sim_dataset_path
            out_file.attrs['real_source'] = real_dataset_path

    print(f"\n{'='*70}")
    print(f"✓ HPO dataset created successfully!")
    print(f"  Output: {output_path}")
    print(f"  Total samples: {sample_idx}")
    print(f"  Sim: {len(valid_sim)} ({len(valid_sim)/sample_idx:.1%})")
    print(f"  Real: {len(valid_real)} ({len(valid_real)/sample_idx:.1%})")
    print(f"{'='*70}\n")


def inspect_hpo_dataset(dataset_path: str) -> None:
    """Inspect and print statistics about HPO dataset.

    Args:
        dataset_path: Path to HPO dataset HDF5
    """
    if not Path(dataset_path).exists():
        print(f"Dataset not found: {dataset_path}")
        return

    print(f"\n{'='*70}")
    print(f"HPO Dataset Inspection: {dataset_path}")
    print(f"{'='*70}\n")

    with h5py.File(dataset_path, 'r') as f:
        # Print metadata
        print("Metadata:")
        for key, value in f.attrs.items():
            print(f"  {key}: {value}")

        # Count samples by source
        samples = list_samples(f)
        num_sim = sum(1 for s in samples if f[s].attrs.get('source') == 'sim')
        num_real = sum(1 for s in samples if f[s].attrs.get('source') == 'real')

        print(f"\nSample Statistics:")
        print(f"  Total samples: {len(samples)}")
        print(f"  Sim samples: {num_sim}")
        print(f"  Real samples: {num_real}")

        # Sequence length statistics
        seq_lengths = []
        for sample in samples:
            if 'sequence_length' in f[sample].attrs:
                seq_lengths.append(f[sample].attrs['sequence_length'])
            else:
                seq_lengths.append(f[sample]['sensor_data'].shape[0])

        if seq_lengths:
            print(f"\nSequence Length Statistics:")
            print(f"  Mean: {np.mean(seq_lengths):.1f}")
            print(f"  Min: {np.min(seq_lengths)}")
            print(f"  Max: {np.max(seq_lengths)}")
            print(f"  Total timesteps: {np.sum(seq_lengths)}")

        # Sample data shapes
        if len(samples) > 0:
            first_sample = f[samples[0]]
            print(f"\nData Shapes (first sample):")
            print(f"  sensor_data: {first_sample['sensor_data'].shape}")
            print(f"  gt_pos_data: {first_sample['gt_pos_data'].shape}")
            print(f"  gt_vel_data: {first_sample['gt_vel_data'].shape}")
            if 'gt_gravity_b_data' in first_sample:
                print(f"  gt_gravity_b_data: {first_sample['gt_gravity_b_data'].shape}")

    print(f"\n{'='*70}\n")


def main():
    parser = argparse.ArgumentParser(
        description="Create mixed sim-real dataset for HPO",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Create default HPO dataset (100 samples, 80% sim)
  python utils/create_hpo_dataset.py

  # Create small HPO dataset for quick testing
  python utils/create_hpo_dataset.py --num-samples 50 --sim-ratio 0.9

  # Create larger HPO dataset with more real data
  python utils/create_hpo_dataset.py --num-samples 200 --sim-ratio 0.7

  # Inspect existing HPO dataset
  python utils/create_hpo_dataset.py --inspect
        """
    )

    # Paths
    parser.add_argument(
        '--sim-dataset',
        type=str,
        default=Config.SIMDATA.DEFAULT_PATH,
        help='Path to simulated dataset HDF5 (default: data/simulated_dataset.h5)'
    )
    parser.add_argument(
        '--real-dataset',
        type=str,
        default=Config.DATASET_H5_PATH,
        help='Path to real dataset HDF5 (default: data/dataset.h5)'
    )
    parser.add_argument(
        '--output',
        type=str,
        default='data/hpo_dataset.h5',
        help='Output path for HPO dataset (default: data/hpo_dataset.h5)'
    )

    # Sampling parameters
    parser.add_argument(
        '--num-samples',
        type=int,
        default=100,
        help='Total number of samples in HPO dataset (default: 100)'
    )
    parser.add_argument(
        '--sim-ratio',
        type=float,
        default=Config.SIMDATA.DEFAULT_MIX_RATIO,
        help='Ratio of simulated samples, 0.0-1.0 (default: 0.8)'
    )
    parser.add_argument(
        '--seed',
        type=int,
        default=42,
        help='Random seed for reproducibility (default: 42)'
    )

    # Inspection mode
    parser.add_argument(
        '--inspect',
        action='store_true',
        help='Inspect existing HPO dataset instead of creating new one'
    )

    args = parser.parse_args()

    # Validate sim_ratio
    if not 0.0 <= args.sim_ratio <= 1.0:
        raise ValueError(f"sim_ratio must be between 0.0 and 1.0, got {args.sim_ratio}")

    if args.inspect:
        # Inspection mode
        inspect_hpo_dataset(args.output)
    else:
        # Creation mode
        create_hpo_dataset(
            sim_dataset_path=args.sim_dataset,
            real_dataset_path=args.real_dataset,
            output_path=args.output,
            num_samples=args.num_samples,
            sim_ratio=args.sim_ratio,
            seed=args.seed
        )

        # Auto-inspect after creation
        inspect_hpo_dataset(args.output)


if __name__ == '__main__':
    main()
